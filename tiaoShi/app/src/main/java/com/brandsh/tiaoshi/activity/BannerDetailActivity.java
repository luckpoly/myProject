    @Override
    public void onBackPressed() {
        if (banner_wv.canGoBack()) {
            banner_wv.goBack();
        } else {
            banner_wv.clearCache(true);
            banner_wv.clearHistory();
            super.onBackPressed();
        }
    }
    /**
     * 显示进度框
     */
    private void showProgressDialog() {
        if (progDialog == null)
            progDialog = new ProgressDialog(this);
        progDialog.setProgressStyle(ProgressDialog.STYLE_SPINNER);
        progDialog.setIndeterminate(false);
        progDialog.setCancelable(true);
        progDialog.setMessage("网页数据加载中");
        progDialog.show();
    }

    /**
     * 隐藏进度框
     */
    private void dissmissProgressDialog() {
        if (progDialog != null) {
            progDialog.dismiss();
        }
    }

    private void init() {
        banner_ivBack.setOnClickListener(this);
        banner_tvTitle.setText(getIntent().getStringExtra("title"));
        WebSettings settings = banner_wv.getSettings();
        settings.setJavaScriptEnabled(true);
        settings.setCacheMode(WebSettings.LOAD_NO_CACHE);
        banner_wv.loadUrl(getIntent().getStringExtra("url"));
        banner_wv.setWebChromeClient(mWebChromeClient);

        banner_wv.setDownloadListener(new DownloadListener() {
            @Override
            public void onDownloadStart(String url, String userAgent, String contentDisposition, String mimetype, long contentLength) {
                Uri uri = Uri.parse(url);
                Intent intent = new Intent(Intent.ACTION_VIEW, uri);
                startActivity(intent);
            }
        });
        banner_wv.setWebViewClient(new WebViewClient() {
            @Override
            public void onPageStarted(WebView view, String url, Bitmap favicon) {
                super.onPageStarted(view, url, favicon);
                showProgressDialog();
            }

            @Override
            public void onPageFinished(WebView view, String url) {
                super.onPageFinished(view, url);
                dissmissProgressDialog();
            }
        });
    }

    @Override
    public void onClick(View v) {
        switch (v.getId()) {
            case R.id.banner_ivBack:
                banner_wv.clearCache(true);
                banner_wv.clearHistory();
                finish();
                break;
        }
    }

